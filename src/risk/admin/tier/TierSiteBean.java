package risk.admin.tier;

public class TierSiteBean {

	private String ts_seq;
	private String ts_name;
	private String ts_type;
	private String ts_rank;
	
	public String getTs_seq() {
		return ts_seq;
	}
	public void setTs_seq(String tsSeq) {
		ts_seq = tsSeq;
	}
	public String getTs_name() {
		return ts_name;
	}
	public void setTs_name(String tsName) {
		ts_name = tsName;
	}
	public String getTs_type() {
		return ts_type;
	}
	public void setTs_type(String tsType) {
		ts_type = tsType;
	}
	public String getTs_rank() {
		return ts_rank;
	}
	public void setTs_rank(String tsRank) {
		ts_rank = tsRank;
	}		
}
