package risk.admin.pressgroup;

public class PressgroupDataBean {
	String pg_seq = "";
	
	String pg_dept = "";
	String pg_title = "";
	String pg_content = "";
	String pg_date = "";
	String pg_regdate = "";
		
	
	public String getPg_dept() {
		return pg_dept;
	}
	public void setPg_dept(String pg_dept) {
		this.pg_dept = pg_dept;
	}
	public String getPg_seq() {
		return pg_seq;
	}
	public void setPg_seq(String pg_seq) {
		this.pg_seq = pg_seq;
	}
	public String getPg_title() {
		return pg_title;
	}
	public void setPg_title(String pg_title) {
		this.pg_title = pg_title;
	}
	public String getPg_content() {
		return pg_content;
	}
	public void setPg_content(String pg_content) {
		this.pg_content = pg_content;
	}
	public String getPg_date() {
		return pg_date;
	}
	public void setPg_date(String pg_date) {
		this.pg_date = pg_date;
	}
	public String getPg_regdate() {
		return pg_regdate;
	}
	public void setPg_regdate(String pg_regdate) {
		this.pg_regdate = pg_regdate;
	}
}
