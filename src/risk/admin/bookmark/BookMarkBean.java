package risk.admin.bookmark;

public class BookMarkBean {
	private String b_seq;
	private String b_mark_seq;
	private String b_contents;
	
	public String getB_seq() {
		return b_seq;
	}
	public void setB_seq(String bSeq) {
		b_seq = bSeq;
	}
	public String getB_mark_seq() {
		return b_mark_seq;
	}
	public void setB_mark_seq(String bMarkSeq) {
		b_mark_seq = bMarkSeq;
	}
	public String getB_contents() {
		return b_contents;
	}
	public void setB_contents(String bContents) {
		b_contents = bContents;
	}	
}
